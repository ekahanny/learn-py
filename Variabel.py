# Variabel adalah tempat menyimpan data
""" Penamaan variabel hanya bisa diawali dengan 
huruf dan underscore (_) & tidak boleh ada spasi 
dalam penamaan variabel """

a = 9
_x = 10
iniVariabel3 = 19000
iniVariabel4 = 150


# Pemanggilan pertama
print("Nilai a = ", a)
print("Nilai x = ", _x)
print("Nilai variabel ketiga = ", iniVariabel3)
print("Nilai variabel keempat = ", iniVariabel4)


# Pemanggilan Kedua
a = 7
print("Nilai a yang baru = ", a)

# Assignment Indirect
b = a
print("Nilai b = ", b)



